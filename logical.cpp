#include<iostream>

using namespace std;

int main(){
// &&(Logical And):
// we have two condition and both are True or same then give True otherwie gives false
bool con1= 1;
bool con2= 0;

cout<<(con1&&con2)<<endl;






// || (Logical or):
// we have two condition and one are True or same then give True otherwie gives false
cout<<(con1||con2)<<endl;

// !(Logical Not): if true then give False
cout<<(!con1)<<endl;

return 0;
}
