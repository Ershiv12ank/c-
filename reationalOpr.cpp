
#include<iostream>

using namespace std;

int main(){
// Relational operators gives result in the form of True and False

//---> == (is equal to): When we have two condition 
//  condition1 == condition2
// if both are true or equal then given True other wise give False
int a=4,b=6,c=4;
cout<<(a==b)<<endl;
cout<<(a==c)<<endl;



//---> ! (not equal to )
cout<<(a!=b)<<endl;
cout<<(a!=c)<<endl;

//---> greaterthan (<)
cout<<(a<b)<<endl;
cout<<(a<c)<<endl;

// ----> smallerthen (>)
cout<<(a>b)<<endl;
cout<<(a>c)<<endl;

// ---> greaterthen equal to <= 
cout<<(a<=b)<<endl;
cout<<(a<=c)<<endl;
// --->smallerthen equal   >=
cout<<(a>=b)<<endl;
cout<<(a>=c)<<endl;





    return 0; 
}